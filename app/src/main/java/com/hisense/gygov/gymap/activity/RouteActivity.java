    private void parseIntent() {
        Intent intent = getIntent();
        Bundle bundle = intent.getBundleExtra(START_ACTIVITY_SAFETY_KEY);
        BDLocation location=bundle.getParcelable("current_LatLng");
        currentLatLng=new LatLng(location.getLatitude(),location.getLongitude());
        poiInfo=bundle.getParcelable("target_LatLng");
        targetLatLng=poiInfo.location;
        targetName=poiInfo.getName();
    }

    private void initView() {
        mMapView=findViewById(R.id.map);
        mBaiduMap=mMapView.getMap();
        back=findViewById(R.id.route_title_back);
        back.setOnClickListener(new View.OnClickListener() {
            @Override
            public void onClick(View view) {
                finish();
            }
        });
        mBaiduMap.setOnMapClickListener(this);
        mTargetText=findViewById(R.id.route_destination_value);
        mSearch = RoutePlanSearch.newInstance();
        mSearch.setOnGetRoutePlanResultListener(this);
        mTargetText.setText(targetName);
    }

    private void initMap() {
        //获取地图控件引用
        mBaiduMap = mMapView.getMap();
        //默认显示普通地图
        mBaiduMap.setMapType(BaiduMap.MAP_TYPE_NORMAL);
        // 开启定位图层
        mBaiduMap.setMyLocationEnabled(true);
        MapStatus.Builder builder = new MapStatus.Builder();
        builder.overlook(-45);
        mBaiduMap.animateMapStatus(MapStatusUpdateFactory.newMapStatus(builder.build()));
    }

    private void initData() {
        stNode=PlanNode.withLocation(currentLatLng);
        enNode=PlanNode.withLocation(targetLatLng);
        mSearch.walkingSearch(new WalkingRoutePlanOption().from(stNode).to(enNode));
    }

