    /**
     * 路线搜索
     * @param view
     */
    public void searchButtonProcess(View view) {
        route=null;
        mBaiduMap.clear();
        stNode=PlanNode.withLocation(currentLatLng);
        enNode=PlanNode.withLocation(targetLatLng);

        if (view.getId()==R.id.car) {
            mSearch.drivingSearch((new DrivingRoutePlanOption())
                    .from(stNode).to(enNode));
            nowSearchType = 0;
        } else if (view.getId() == R.id.bus) {
            mSearch.transitSearch((new TransitRoutePlanOption())
                    .from(stNode).city("青岛").to(enNode));
            nowSearchType = 1;
        } else if (view.getId() == R.id.walk) {
            mSearch.walkingSearch((new WalkingRoutePlanOption())
                    .from(stNode).to(enNode));
            nowSearchType = 2;
        } else if (view.getId() == R.id.bike) {
            mSearch.bikingSearch((new BikingRoutePlanOption())
                    .from(stNode).to(enNode));
            nowSearchType = 3;
        }
    }

